export default function initTemplateModel(sequelize, DataTypes) {
  return sequelize.define(
    'template',
    {
      id: {
        allowNull: false,
        autoIncrement: true,
        primaryKey: true,
        type: DataTypes.INTEGER,
      },
      name: {
        type: DataTypes.STRING,
      },
      htmlContent: {
        type: DataTypes.TEXT,
      },
      htmlSampleContent: {
        type: DataTypes.TEXT,
      },
      htmlArticleTemplate: {
        type: DataTypes.TEXT,
      },
      createdAt: {
        allowNull: false,
        type: DataTypes.DATE,
      },
      updatedAt: {
        allowNull: false,
        type: DataTypes.DATE,
      },
    },
    {
      // The underscored option makes Sequelize reference snake_case names in the DB.
      underscored: true,
    },
  );
}
